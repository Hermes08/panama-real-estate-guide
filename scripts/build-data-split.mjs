#!/usr/bin/env node
// =============================================================================
// build-data-split.mjs — split project/data.js into a light core + per-article
//                       body files so pages don't ship the full 1.8 MB on
//                       every pageview
// =============================================================================
// Source of truth stays as project/data.js (committed to git, edited by hand
// or by inject scripts). This build step (run before inject-article-meta) reads
// data.js, strips articleBodies, and writes two artifacts:
//
//   project/data-light.js          — everything except articleBodies (~43 KB)
//   project/bodies/articles/*.js   — one file per slug, hydrates only that
//                                    article's body into window.PANAMA_DATA
//
// HTML shells load BOTH: data-light.js for the index/listing metadata, then
// the per-slug body file (injected into each article shell by
// inject-article-meta.mjs). Project/video/news/index pages only load
// data-light.js since they never read articleBodies.
//
// newsBodies stays in data-light.js (17 KB total, not worth splitting).
//
// Outputs are gitignored — regenerated on every deploy.
// =============================================================================

import fs from 'node:fs/promises';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_DIR = path.resolve(__dirname, '..', 'project');
const DATA_JS = path.join(PROJECT_DIR, 'data.js');
const DATA_LIGHT_JS = path.join(PROJECT_DIR, 'data-light.js');
const BODIES_ARTICLES_DIR = path.join(PROJECT_DIR, 'bodies', 'articles');

async function main() {
  const src = await fs.readFile(DATA_JS, 'utf8');
  const sandbox = { window: {}, console: { log() {}, warn() {}, error() {} } };
  vm.createContext(sandbox);
  vm.runInContext(src, sandbox, { timeout: 5000 });
  const D = sandbox.window.PANAMA_DATA;
  if (!D) throw new Error('PANAMA_DATA missing from data.js');

  const articleBodies = D.articleBodies || {};
  const slugs = Object.keys(articleBodies);

  // Build light copy — preserves everything except articleBodies.
  const light = { ...D };
  delete light.articleBodies;

  // Write data-light.js. Keep the same window.PANAMA_DATA = {...} shape that
  // every HTML shell expects, so the only change at the call site is the
  // <script src> filename.
  await fs.writeFile(
    DATA_LIGHT_JS,
    `// Auto-generated by scripts/build-data-split.mjs. Do not edit by hand.\n` +
      `// Source: project/data.js. articleBodies live in project/bodies/articles/{slug}.js.\n` +
      `window.PANAMA_DATA = ${JSON.stringify(light)};\n`,
    'utf8'
  );

  // Write per-slug body files.
  await fs.mkdir(BODIES_ARTICLES_DIR, { recursive: true });
  let bodyBytes = 0;
  for (const slug of slugs) {
    const body = articleBodies[slug];
    const out = path.join(BODIES_ARTICLES_DIR, `${slug}.js`);
    // Each body file hydrates only its own slug onto the existing
    // window.PANAMA_DATA. Defensive about PANAMA_DATA already existing
    // because the load order is data-light → body, but defensive is cheap.
    const content =
      `window.PANAMA_DATA = window.PANAMA_DATA || {};\n` +
      `window.PANAMA_DATA.articleBodies = window.PANAMA_DATA.articleBodies || {};\n` +
      `window.PANAMA_DATA.articleBodies[${JSON.stringify(slug)}] = ${JSON.stringify(body)};\n`;
    await fs.writeFile(out, content, 'utf8');
    bodyBytes += content.length;
  }

  // Cleanup: remove stale body files for slugs that no longer exist in data.js.
  const expected = new Set(slugs.map(s => `${s}.js`));
  const existing = await fs.readdir(BODIES_ARTICLES_DIR).catch(() => []);
  let removed = 0;
  for (const f of existing) {
    if (!f.endsWith('.js')) continue;
    if (!expected.has(f)) {
      await fs.unlink(path.join(BODIES_ARTICLES_DIR, f));
      removed++;
    }
  }

  const lightBytes = (await fs.stat(DATA_LIGHT_JS)).size;
  const fullBytes = src.length;
  const savingsPct = ((fullBytes - lightBytes) / fullBytes * 100).toFixed(1);
  console.log(
    `build-data-split: data-light=${(lightBytes/1024).toFixed(1)}KB, ` +
    `${slugs.length} body files (~${(bodyBytes/1024/slugs.length).toFixed(1)}KB avg, ` +
    `${(bodyBytes/1024/1024).toFixed(2)}MB total), ` +
    `${removed} stale removed. ` +
    `data.js was ${(fullBytes/1024/1024).toFixed(2)}MB; light is ${savingsPct}% smaller.`
  );
}

main().catch(err => { console.error('build-data-split failed:', err); process.exit(1); });
